package basics_01;

public class charDatatype {
    static void main() {

        int b=65;

        char ch=(char)b;
        System.out.println(ch);
        char a='b';
        System.out.println(a);
        System.out.println((int)a); // can be used to show int
        System.out.println(a+a);
        System.out.println(a*a);
    }
}
